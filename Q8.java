// 8. Age in Future
// Write a program that takes your current age and calculates how old you will be in 10 years.
// Example output:
// You are 20 years old now. In 10 years, you will be 30.


public class Q8 {
    public static void main(String[] args) {
        
        int currentAge = 18;

        int futureAge = currentAge + 10;

        System.out.println("You are " + currentAge + " years old now. In 10 years, you will be " + futureAge + ".");

    }
}
