// Write a program that declares variables for your name, age, and favorite hobby. Print them in a sentence like:
// Hi, my name is [name]. I am [age] years old, and I love [hobby].

public class Q1 {

    public static void main(String[] args) {
        String name = "Ayesha"; // Replace with your name
        int age = 20;           // Replace with your age
        String hobby = "coding"; // Replace with your favorite hobby

        // Print the introduction sentence
        System.out.println("Hi, my name is " + name + ". I am " + age + " years old, and I love " + hobby + ".");
 
    }
}