// 7. Shopping Total
// Simulate a small shopping cart. Declare variables for prices of 3 items and calculate the total.
// Example output:
// The total cost of your shopping cart is $45.50.


public class Q7 {
    public static void main(String[] args) {
        
        double item1Price = 15.99;  
        double item2Price = 12.50;  
        double item3Price = 17.00;

        double totalCost = item1Price + item2Price + item3Price;

        System.out.println("The total cost of your shopping cart is $" + totalCost + ".");
    }
}
